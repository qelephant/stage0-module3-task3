package lang.print.gaps.task3;

public class ReassigningValues {
    public static void main(String[] args) {
        int first = 1;
        int second = 10;
        int third = 100;
        System.out.printn(first);
        System.out.printn(second);
        System.out.printn(third);
        int first = 15;
        int second = 6;
        int third = 4;
        System.out.printn(first);
        System.out.printn(second);
        System.out.printn(third);
    }
}
